package br.documentation.dto;

public class DocumentRequestDTO {
    private String url;
    private String folder;

    // Getters e setters

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getFolder() {
        return folder;
    }

    public void setFolder(String folder) {
        this.folder = folder;
    }
}

